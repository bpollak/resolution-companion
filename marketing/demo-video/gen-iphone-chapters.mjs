#!/usr/bin/env node
/**
 * Caption timings for the real-device promo's side-caption (landscape) cuts —
 * both the master (iphone-screen.mp4) and the social short
 * (iphone-screen-short.mp4). Anchored to segment name + offset so they track
 * the footage when build-iphone*.sh change the ramps.
 *
 *   node gen-iphone-chapters.mjs   ->  ../promo-video/src/iphone-chapters.ts
 */
import { execFileSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const dur = (f) =>
  parseFloat(
    execFileSync(
      "ffprobe",
      ["-v", "error", "-show_entries", "format=duration", "-of", "csv=p=0", join(HERE, f)],
      { encoding: "utf8" },
    ).trim(),
  );

const timeline = (keys) => {
  const map = {};
  let t = 0;
  for (const k of keys) {
    map[k] = t;
    t += dur(`seg/${k}.mp4`);
  }
  map.__total = t;
  return map;
};

const MASTER_ORDER = [
  "i1a", "i1b", "i1c", "i1d", "i1e", "i1f", "i1g", "i2a",
  "i3a", "i3b", "i3c", "i3d",
  "i4a", "i4b", "i4c", "i4d", "i4e", "i4f", "i4g", "i4h", "i4i",
];
const SHORT_ORDER = ["is1", "is2", "is3", "is4", "is5", "is6", "is7", "is8"];

const M = timeline(MASTER_ORDER);
const S = timeline(SHORT_ORDER);

// [segment, offsetSeconds, kicker, line, accent]
const MASTER = [
  ["i1a", 0, "The premise", "Don't set a goal.", "Become someone."],
  ["i1b", 0, "Two minutes", "An AI coach asks who", "you're becoming."],
  ["i1d", 1.0, "Your plan, written", "It turns your goal into", "real habits."],
  ["i1g", 1.0, "Built for you", "Milestones and small", "daily actions."],
  ["i2a", 1.5, "The daily loop", "Check it off.", "Day complete."],
  ["i3a", 0, "Your progress", "A calendar that fills,", "never resets."],
  ["i3b", 0, "Make it yours", "Edit milestones and", "target dates."],
  ["i4b", 0, "Your coach checks in", "“One win from", "last week?”"],
  ["i4f", 1.0, "Real coaching", "It adapts the plan to", "how you're doing."],
  ["i4h", 0.5, "In your corner", "Specific. Personal.", "Every week."],
];

const SHORT = [
  ["is1", 0, "Two minutes", "Tell an AI coach", "your goal."],
  ["is3", 0.8, "Your plan, written", "It becomes", "real habits."],
  ["is4", 0, "Built for you", "Milestones and daily", "actions."],
  ["is5", 1.5, "The daily loop", "Check it off.", "Day complete."],
  ["is6", 0.2, "Your coach", "Tell it what's", "hard."],
  ["is8", 1.0, "Real coaching", "It adapts to", "how you're doing."],
];

const emit = (rows, map) =>
  rows
    .map(([seg, off, kicker, line, accent]) => {
      const at = +(map[seg] + off).toFixed(2);
      return `  { at: ${at}, kicker: ${JSON.stringify(kicker)}, line: ${JSON.stringify(
        line,
      )}, accent: ${JSON.stringify(accent)} }, // ${seg}${off ? ` +${off}s` : ""}`;
    })
    .join("\n");

const out = `// GENERATED by marketing/demo-video/gen-iphone-chapters.mjs — do not hand-edit.
// Side captions for the real-device promo's landscape cuts, derived from the
// segment durations build-iphone*.sh produce.

export interface Chapter {
  at: number;
  kicker: string;
  line: string;
  accent?: string;
}

export const IPHONE_MASTER_SECONDS = ${M.__total.toFixed(2)};
export const IPHONE_SHORT_SECONDS = ${S.__total.toFixed(2)};

export const IPHONE_CHAPTERS: Chapter[] = [
${emit(MASTER, M)}
];

export const IPHONE_SHORT_CHAPTERS: Chapter[] = [
${emit(SHORT, S)}
];
`;

writeFileSync(join(HERE, "..", "promo-video", "src", "iphone-chapters.ts"), out);
console.log(`master ${M.__total.toFixed(2)}s · short ${S.__total.toFixed(2)}s`);
console.log("\nmaster captions:");
for (const [seg, off, , line] of MASTER) console.log(`  ${(M[seg] + off).toFixed(1).padStart(6)}s  ${line}`);
console.log("\nshort captions:");
for (const [seg, off, , line] of SHORT) console.log(`  ${(S[seg] + off).toFixed(1).padStart(6)}s  ${line}`);
