#!/usr/bin/env node
/**
 * Generates the caption timings for both cuts, straight from the segments the
 * edit actually produced.
 *
 * The first version of this was hand-timed, and every caption drifted out of
 * sync with the footage — "Finish the day, and it says so" played over a
 * half-finished day. Captions are now anchored to a SEGMENT NAME plus an
 * offset, so they follow the edit automatically whenever the cut changes.
 *
 *   node gen-chapters.mjs   ->  ../promo-video/src/chapters.ts
 */
import { execFileSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const dur = (f) =>
  parseFloat(
    execFileSync(
      "ffprobe",
      ["-v", "error", "-show_entries", "format=duration", "-of", "csv=p=0", join(HERE, f)],
      { encoding: "utf8" },
    ).trim(),
  );

/** Cumulative start time of each segment, in order. */
const timeline = (files) => {
  const map = {};
  let t = 0;
  for (const f of files) {
    map[f.key] = t;
    t += dur(f.path);
  }
  map.__total = t;
  return map;
};

const MASTER_ORDER = [
  "a1-coldopen", "a2-carousel", "a3-consent", "a4-coachopen", "a5-answer1",
  "a7-planready", "a8-building", "b1-today", "b2-scroll",
  "b3-checkoff", "b4-milestone", "b5-daycomplete", "b6-note", "b7-journey",
  "b8-milestones", "b9-coachhome", "b10-exchange",
];
const SHORT_ORDER = ["s-1", "s-2", "s-3", "s-4", "s-5", "s-6", "s-7", "s-8", "s-9"];

const M = timeline(MASTER_ORDER.map((k) => ({ key: k, path: `seg/${k}.mp4` })));
const S = timeline(SHORT_ORDER.map((k) => ({ key: k, path: `seg/${k}.mp4` })));

/**
 * [segment, offsetSeconds, kicker, line, accent]
 * The offset exists because a beat doesn't always land on a segment boundary —
 * the day, for instance, doesn't actually complete until ~2.5s into b5.
 */
const MASTER = [
  ["a1-coldopen", 0, "The premise", "Don't set a goal.", "Become someone."],
  ["a4-coachopen", 0, "Two minutes", "An AI coach asks who you're", "becoming."],
  ["a8-building", 0, "Your plan, built", "Milestones and small daily", "actions."],
  ["b1-today", 0, "The daily loop", "Two small things.", "That's the day."],
  ["b3-checkoff", 1.2, "Every action counts", "Each one is a", "vote."],
  ["b4-milestone", 1.0, "21 days", "Not a plan anymore. A", "habit."],
  ["b5-daycomplete", 4.6, "The payoff", "Finish the day, and it", "says so."],
  ["b7-journey", 0, "No guilt", "A missed day never", "erases you."],
  // Anchor to the coach HOME (not the exchange) so "It remembers" lands as the
  // Coach screen first appears, instead of the no-guilt caption holding ~6s over it.
  ["b9-coachhome", 0.3, "It remembers", "A coach that reads your", "notes."],
  ["b10-exchange", 1.5, "In your corner", "You name a win — it answers,", "personally."],
];

const SHORT = [
  ["s-1", 0, "The premise", "Don't set a goal.", "Become someone."],
  ["s-2", 0, "Two minutes", "Tell an AI coach who you want to", "be."],
  ["s-4", 0, "Your plan, built", "It writes the", "milestones."],
  ["s-5", 0.5, "The daily loop", "Every action is a", "vote."],
  ["s-6", 1.0, "21 days", "Not a plan. A", "habit."],
  ["s-7", 0.5, "The payoff", "Finish the day, and it", "says so."],
  ["s-8", 0, "No guilt", "A missed day never", "erases you."],
  ["s-9", 1.5, "It remembers", "A coach in your", "corner."],
];

const emit = (rows, map) =>
  rows
    .map(([seg, off, kicker, line, accent]) => {
      const at = +(map[seg] + off).toFixed(2);
      return `  { at: ${at}, kicker: ${JSON.stringify(kicker)}, line: ${JSON.stringify(
        line,
      )}, accent: ${JSON.stringify(accent)} }, // ${seg}${off ? ` +${off}s` : ""}`;
    })
    .join("\n");

const out = `// GENERATED by marketing/demo-video/gen-chapters.mjs — do not hand-edit.
// Timings are derived from the segments the edit actually produced, so they
// stay in sync with the footage when the cut changes.

export interface Chapter {
  at: number;
  kicker: string;
  line: string;
  accent?: string;
}

export const MASTER_SECONDS = ${M.__total.toFixed(2)};
export const SHORT_SECONDS = ${S.__total.toFixed(2)};

export const CHAPTERS: Chapter[] = [
${emit(MASTER, M)}
];

export const SHORT_CHAPTERS: Chapter[] = [
${emit(SHORT, S)}
];
`;

writeFileSync(join(HERE, "..", "promo-video", "src", "chapters.ts"), out);
console.log(`master ${M.__total.toFixed(2)}s · short ${S.__total.toFixed(2)}s`);
console.log("\nmaster captions:");
for (const [seg, off, , line] of MASTER)
  console.log(`  ${(M[seg] + off).toFixed(1).padStart(6)}s  ${line}`);
console.log("\nshort captions:");
for (const [seg, off, , line] of SHORT)
  console.log(`  ${(S[seg] + off).toFixed(1).padStart(6)}s  ${line}`);
